void help()
{
    setcolor(LIGHTGRAY);
    setfillstyle(SOLID_FILL,LIGHTGRAY);
    fillpoly(4,background_coor);
    settextjustify(1,1);
    settextstyle(3,0,4);
    setcolor(DARKGRAY);
    outtextxy(320,20,"Help");
    getch();
    settextjustify(0,1);
    settextstyle(1,0,1);
    outtextxy(10,80,"1. Use arrow keys to move the character.");
    getch();
    outtextxy(10,100,"2. Use Spacebar to select");
    getch();
    outtextxy(10,120,"3. Use Esc to save and exit to menu.");
    getch();
}
